def __string_convert(string) -> str:
    buffer = 0
    if len(string) % 2:
        buffer = 1

    formated = '|'
    str_buffer = -1 * (len(string) - 17) // 2

    formated += ' ' * str_buffer
    formated += string
    formated += ' ' * (str_buffer - buffer)
    formated += '|'

    return formated


